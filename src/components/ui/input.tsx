import * as React from "react";
import { cn } from "../../lib/utils";

interface InputProps extends React.ComponentProps<"input"> {
  error?: boolean | string;
}

function Input({
  className,
  type,
  error,
  ...props
}: InputProps) {
  return (
    <input
      type={type}
      data-slot="input"
      aria-invalid={error ? "true" : "false"}
      className={cn(
        "file:text-foreground placeholder:text-muted-foreground selection:bg-primary selection:text-primary-foreground dark:bg-input/30",
        "border-input h-9 w-full min-w-0 rounded-md border bg-transparent px-3 py-1 text-base shadow-xs outline-none",
        "transition-all duration-200 file:inline-flex file:h-7 file:border-0 file:bg-transparent file:text-sm file:font-medium",
        "disabled:pointer-events-none disabled:cursor-not-allowed disabled:opacity-50 md:text-sm",

        // Focus states
        !error && "focus:border-blue-500 focus:ring-[3px] focus:ring-blue-500/20",

        // Error states
        error &&
          "border-destructive focus:border-destructive focus:ring-[3px] focus:ring-destructive/20",

        className
      )}
      {...props}
    />
  );
}

export { Input };